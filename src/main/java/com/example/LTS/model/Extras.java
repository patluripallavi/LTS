package com.example.LTS.model;

public class Extras {
	
	private String innings;
	
	private String nr;
	
	private String text;
	
	private String total_overs;
	
	private String total;
	
	private String percent_over;

	public String getInnings() {
		return innings;
	}

	public void setInnings(String innings) {
		this.innings = innings;
	}

	public String getNr() {
		return nr;
	}

	public void setNr(String nr) {
		this.nr = nr;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getTotal_overs() {
		return total_overs;
	}

	public void setTotal_overs(String total_overs) {
		this.total_overs = total_overs;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getPercent_over() {
		return percent_over;
	}

	public void setPercent_over(String percent_over) {
		this.percent_over = percent_over;
	}
	

	
}
