package com.example.LTS.model;

public class Bowling {
	
	private String innings;
	
	private String nr;
	
	private String balls;
	
	private String runs;
	
	private String bowler_to_batsma;

	public String getInnings() {
		return innings;
	}

	public void setInnings(String innings) {
		this.innings = innings;
	}

	public String getNr() {
		return nr;
	}

	public void setNr(String nr) {
		this.nr = nr;
	}

	public String getBalls() {
		return balls;
	}

	public void setBalls(String balls) {
		this.balls = balls;
	}

	public String getRuns() {
		return runs;
	}

	public void setRuns(String runs) {
		this.runs = runs;
	}

	public String getBowler_to_batsma() {
		return bowler_to_batsma;
	}

	public void setBowler_to_batsman(String bowler_to_batsma) {
		this.bowler_to_batsma = bowler_to_batsma;
	}
	
	
}
